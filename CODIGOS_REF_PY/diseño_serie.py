# --- DATOS GENERALES ---------------------------------------------------------

CABEZA_TOTAL    = 35.843      # [metros de agua columna, mca]
CABEZA_LLEGADA  = 0.0       # [metros de agua columna, mca]
DENSIDAD        = 999.1    # [kg/m³]
VISCOSIDAD      = 0.00114     # [Pa·s  =  kg/(m·s)]

# --- DIÁMETROS DISPONIBLES ---------------------------------------------------
# Lista de diámetros comerciales entre los cuales el programa elegirá
# Unidad: metros [m]

DIAMETROS_DISPONIBLES = [0.050, 0.075, 0.100, 0.125, 0.150, 0.200, 0.250, 0.300, 0.350, 0.400, 0.450, 0.500]

# --- TUBERÍAS ----------------------------------------------------------------
